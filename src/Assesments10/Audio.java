public class Audio {
    public void play() {
        System.out.println("Playing audio");
    }

    public void pause() {
        System.out.println("Pausing audio");
    }

    public void resume() {
        System.out.println("Resuming audio");
    }

    public void stop() {
        System.out.println("Stopping audio");
    }

    public void adjustVolume() {
        System.out.println("Adjusting audio volume");
    }
}
