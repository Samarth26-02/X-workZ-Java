package Assesments10;

public class Alarm {
    public void arm() {
        System.out.println("Arming alarm");
    }

    public void disarm() {
        System.out.println("Disarming alarm");
    }

    public void trigger() {
        System.out.println("Triggering alarm");
    }

    public void silence() {
        System.out.println("Silencing alarm");
    }

    public void checkStatus() {
        System.out.println("Checking alarm status");
    }
}
