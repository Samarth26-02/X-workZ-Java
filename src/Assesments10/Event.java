public class Event {
    public void schedule() {
        System.out.println("Scheduling event");
    }

    public void announce() {
        System.out.println("Announcing event");
    }

    public void start() {
        System.out.println("Starting event");
    }

    public void end() {
        System.out.println("Ending event");
    }

    public void summarize() {
        System.out.println("Summarizing event");
    }
}
