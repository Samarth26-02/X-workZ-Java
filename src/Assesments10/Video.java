package Assesments10;

public class Video {
    public void play() {
        System.out.println("Playing video");
    }

    public void pause() {
        System.out.println("Pausing video");
    }

    public void resume() {
        System.out.println("Resuming video");
    }

    public void stop() {
        System.out.println("Stopping video");
    }

    public void seek() {
        System.out.println("Seeking video");
    }
}
