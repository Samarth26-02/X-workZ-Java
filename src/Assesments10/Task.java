package Assesments10;

public class Task {
    public void start() {
        System.out.println("Starting task");
    }

    public void execute() {
        System.out.println("Executing task");
    }

    public void pause() {
        System.out.println("Pausing task");
    }

    public void resume() {
        System.out.println("Resuming task");
    }

    public void finish() {
        System.out.println("Finishing task");
    }
}
